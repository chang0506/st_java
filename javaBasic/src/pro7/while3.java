package pro7;

public class while3 {

	public static void main(String[] args) {
		int d = 2;
		int n = 3;
		
		System.out.println(d + " x " + n + " = " + (d*n));
		
		int d2 = 2;
		int i = 0;
		while(i<9) {
			int n2 = i + 1;
			System.out.println(d2 + " x " + n2 + " = " + (d2*n2));
			i++;
		}

	}

}
